class Producto:
    def __init__(self, sku, nombre_producto,
                 descripcion_producto, costo_producto, precio_producto, marca, foto_producto):
        self.sku = sku
        self.nombre_producto = nombre_producto
        self.descripcion_producto = descripcion_producto
        self.costo_producto = costo_producto
        self.precio_producto = precio_producto
        self.marca = marca
        self.foto_producto = foto_producto
